package Day03Variables;

public class Escape_Squences {
	
	/*
	 \n  ve \r : starts from the new line.
	 \t : horizantal space (paragraf basi boslugu)
	 \\ : prints a backslash (\)
	 \' : prints single quote (')
	 \" : prints double quote (")
	 
	 
	 
	 */
	
	public static void main(String[] args) {
		System.out.println("Hello  Cybertek \n Batch 12 students");
		
		
		System.out.print("Tomorrow is off \n");
		System.out.print("True \n");
		System.out.print("False \n");
		
		System.out.println(" \t Class starts tomorrow");
		
		System.out.println("\\ Muhtar");
		
		System.out.println("\' Java");
		System.out.println("Book is called \" Game of Thrones\"");
		
		
		
	}
	
	
	
}
